# 以下の a, b, c の合計が知りたい
a = [1, 2, 3, 4, 5]
b = [1, 10, 100, 1000]
c = [5, 10, 15, 20, 25]

d = 0
# a をすべて足し算する
for i in a:
    d = d + i

e = 0
# b をすべて足し算する
for i in b:
    e = e + i

f = 0
# c をすべて足し算する
for i in c:
    f = f + i

print(d, e, f)

# ---以下実行結果 ---
# # a, b, c の合計
# (15, 1111, 75)